import numpy as np
import math
import matplotlib.pyplot as plt

# Input
#########################################################
n = int(input("Donner 1 int : "))
min  = int(input("Donner la valeur MIN : "))
max = int(input("Donner la valeur MAX : "))
s = float(input("Donner sigma : "))
mu = float(input("Donner mu : "))
#########################################################



# Coef de la gaussienne
#########################################################
a = (1/(np.sqrt(2*np.pi*pow(s,2))))
#########################################################


# fonction
#########################################################
f = lambda x: a * np.exp(-(pow((x-mu),2))/2*(pow(s,2)))
#########################################################



# Exemple fonction
########################################################
# -x +20
# np.exp(-pow((x),2) * 532)
# pow(x,2)
#########################################################



# Création liste de x
#########################################################
x = np.linspace(min,max,n+1)
#########################################################



# Appel de la fonction
#########################################################
g = f(x)
#########################################################


# step
#########################################################
step = (max-min)/n
#########################################################


# Initialisation
#########################################################
sr = 0
st = 0
#########################################################


# Boucle de la somme
#########################################################
for  i in range(n):

    # Rectangle

    sr = np.sqrt(pow((step*g[i]),2)) + sr

    # Triangle rectangle

    st = 0.5* step * np.sqrt(pow((g[i+1] - g[i]),2)) + st

    # Somme totale

    s = sr + st
#########################################################



# Affichage de l'Intégrale
#########################################################
print("Intégrale = ",s)
#########################################################



# Affiche de Alpha
#########################################################
alpha = (np.pi)/(pow(s,2))
print("Alpha = ",alpha)
#########################################################


# Affichage de la courbe
#########################################################
plt.plot(x,g)
plt.grid()
plt.show()
#########################################################
